        public void llenarLicenciaturas(){
            AdministrarLicenciaturas l1 = new AdministrarLicenciaturas();
            LinkedList<Licenciatura> lista = l1.listaLicenciaturas();
            for(int i=0; i<lista.size(); ++i){
                Licenciatura lic = new Licenciatura();
                lic=lista.get(i);
               cmbListaLicenciaturas.addItem(lic);
            }
        }
        
        public void llenarAlumnos(String clave){
            AdministrarAlumnos administra = new AdministrarAlumnos();
            LinkedList<Alumno>lista = administra.listaPorCarrera(clave);
            cmbListaAlumnos.removeAllItems();
            for(int i=0; i<lista.size(); ++i){
                Alumno alumno = lista.get(i);
                cmbListaAlumnos.addItem(alumno);
            }
        }

