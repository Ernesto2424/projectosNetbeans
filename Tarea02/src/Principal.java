       public String registroActividades(LinkedList<JCheckBox> lista){
           int contador=0;
           String cadena = "";
           for(int i=0; i<lista.size(); ++i){
               JCheckBox aux = lista.get(i);
               if(aux.isSelected()){
                   cadena += aux.getText() + " ";
                   ++contador;
               }
           }
           if(contador == 0){
               cadena = "NINGUNA";
           }
           
        return cadena;
        
       }
       
       public String getGenero(){
        String genero="";
        
        if(rb_masculino.isSelected()){
            genero = rb_masculino.getLabel();
        }else if(rb_femenino.isSelected()){
            genero = rb_femenino.getLabel();
        }
           return genero;  
       }
       
       public String getDivision(){
           String division = "";
           if(rb_cbi.isSelected()){
               division = rb_cbi.getLabel();
           }else if(rb_csh.isSelected()){
               division = rb_csh.getLabel();
           }else if(rb_cyad.isSelected()){
               division = rb_cyad.getLabel();
           }
           return division;
       }
       
