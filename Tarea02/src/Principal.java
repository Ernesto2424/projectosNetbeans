       public void insertar(){
           String matricula = txt_matricula.getText();
           String apellidoP = txt_apellidoP.getText().toUpperCase();
           String apellidoM = txt_apellidoM.getText().toUpperCase();
           String nombre = txt_nombre.getText().toUpperCase();
           String edad = txt_edad.getText();
           String genero = getGenero();
           String division = getDivision();
           String actividades = registroActividades(this.actividades);
           
           if(matricula.isEmpty() || apellidoP.isEmpty()|| apellidoM.isEmpty()|| nombre.isEmpty()|| edad.isEmpty()){
               JOptionPane.showMessageDialog(null, "TODOS LOS CAMPOS MARCADOS CON * SON OBLIGATORIOS", "ERROR", JOptionPane.ERROR_MESSAGE);
           }else{
           
           String sql = "INSERT INTO persona(matricula, apellidop, apellidom, nombre, edad, genero, division, actividad) VALUES(?, ?, ?, ?, ?, ?, ?, ?)";
           
        try {
            PreparedStatement pst = (PreparedStatement) cn.prepareStatement(sql);
            pst.setString(1, matricula);
            pst.setString(2, apellidoP);
            pst.setString(3, apellidoM);
            pst.setString(4, nombre);
            pst.setString(5, edad);
            pst.setString(6, genero);
            pst.setString(7, division);
            pst.setString(8, actividades);
            pst.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }
            JOptionPane.showMessageDialog(null, "REGISTRO EXITOSO", "REGISTRO", JOptionPane.INFORMATION_MESSAGE);
            limpiarDatos();
           }
       }
       
