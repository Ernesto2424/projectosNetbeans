    private void btn_registroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registroActionPerformed
        String nombre = txt_nombre.getText();
        String apellidoP = txt_apellidoP.getText();
        String apellidoM = txt_apellidoM.getText();
        String rfc = txt_rfc.getText();
        String direccion = txt_direccion.getText();
        
        if(rfc.length()==10){
        pn.setNombre(nombre);
        pn.setAp(apellidoP);
        pn.setAm(apellidoM);
        pn.setRfc(rfc.toUpperCase());
        pn.setDir(direccion);
        JOptionPane.showMessageDialog(null, "Registrado con exito\n"
                + pn.toString());
        borrar_campos();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "RFC no valido");
        }
        
    }//GEN-LAST:event_btn_registroActionPerformed

    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        String nombre = txt_nombre.getText();
        String apellidoP = txt_apellidoP.getText();
        String apellidoM = txt_apellidoM.getText();
        String rfc = txt_rfc.getText();
        String direccion = txt_direccion.getText();
        
        if(nombre.isEmpty() && apellidoP.isEmpty() && apellidoM.isEmpty() && rfc.isEmpty() && direccion.isEmpty()){
            JOptionPane.showMessageDialog(null, "campos vacios");
        }else{
            borrar_campos();
        }
    }//GEN-LAST:event_jLabel7MouseClicked

